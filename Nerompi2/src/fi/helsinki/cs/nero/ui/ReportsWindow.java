    private Date parseDate(String text) {
        Date date = new Date();
        if (text != null) {
            String[] split = text.split("\\.");
            date.setDate(new Integer(split[0]));
            date.setMonth(new Integer(split[1]) - 1);
            date.setYear(new Integer(split[2]) - 1900);
        }
        return date;
    }

